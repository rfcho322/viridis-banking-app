export const exchangePublicToken = async ({
  publicToken,
  user,
}: exchangePublicTokenProps) => {
  try {
    // EXCHANGE PUBLIC TOKEN FOR ACCESS TOKEN AND ITEM ID
    const response = await plaidClient.itemPublicTokenExchange({
      public_token: publicToken,
    });

    const accessToken = response.data.access_token;
    const itemId = response.data.item_id;

    // GET ACCOUNT INFORMATION FROM PLAID USING THE ACCESS TOKEN
    const accountResponse = await plaidClient.accountsGet({
      access_token: accessToken,
    });

    const accountData = accountResponse.data.accounts[0];

    // CREATE A PROCESSOR TOKEN FOR DWOLLA USING THE ACCESS TOKEN AND ACCOUNT ID
    const request: ProcessorTokenCreateRequest = {
      access_token: accessToken,
      account_id: accountData.account_id,
      processor: "dwolla" as ProcessorTokenCreateRequestProcessorEnum,
    };

    const processorTokenResponse = await plaidClient.processorTokenCreate(
      request
    );
    const processorToken = processorTokenResponse.data.processor_token;

    // CREATE A FUNDING SOURCE URL FOR THE ACCOUNT USING THE DWOLLA CUSTOMER ID, PROCESSOR TOKEN AND BANK NAME
    const fundingSourceUrl = await addFundingSource({
      dwollaCustomerId: user.dwollaCustomerId,
      processorToken,
      bankName: accountData.name,
    });

    // IF FUNDING SOURCE URL IS NOT CREATED, THROW AN ERROR
    if (!fundingSourceUrl) throw Error;

    // CREATE A BANK ACCOUNT USING THE USER ID, ITEM ID, ACCOUNT ID, ACCESS TOKEN, FUNDING SOURCE URL AND SHAREABLE ID
    await createBankAccount({
      userId: user.$id,
      bankId: itemId,
      accountId: accountData.account_id,
      accessToken,
      fundingSourceUrl,
      shareableId: encryptId(accountData.account_id),
    });

    // REVALIDATE THE PATH TO REFLECT THE CHANGES
    revalidatePath("/");
    // RETURN A SUCCESS MESSAGE
    return parseStringify({
      publicTokenExchange: "complete",
    });
  } catch (error) {
    console.error("An error occured while creating exchange token:", error);
  }
};

